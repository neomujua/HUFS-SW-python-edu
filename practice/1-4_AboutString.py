# String끼리 더하면 글자가 이어집니다.
string_1 = "안녕하세요"
string_2 = "안녕히계세요"
string_3 = string_1+string_2
print(string_3)

# 곱셈도 됩니다.
string_4 = string_3*10
print(string_4)

# \n를 사용하면 한줄이 띄어집니다.
string_n = "안녕하세요\n안녕히계세요"
print(string_n)

# 특수 기호를 넣고 싶을 때는 \를 사용합니다.
string_s = "그가 말했다. \'안녕하세요.\'\n그리고 내가 말했다. \"안녕히계세요.\""
print(string_s)